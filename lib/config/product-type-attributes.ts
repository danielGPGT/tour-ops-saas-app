// Helper function to validate attribute values
export function validateAttributeValue(
  attribute: AttributeField, 
  value: any
): { isValid: boolean; error?: string } {
  if (attribute.required && (!value || value === '')) {
    return { isValid: false, error: `${attribute.label} is required` };
  }

  if (attribute.type === 'number' && value !== undefined && value !== '') {
    const numValue = Number(value);
    if (isNaN(numValue)) {
      return { isValid: false, error: `${attribute.label} must be a number` };
    }
    
    if (attribute.validation?.min !== undefined && numValue < attribute.validation.min) {
      return { isValid: false, error: `${attribute.label} must be at least ${attribute.validation.min}` };
    }
    
    if (attribute.validation?.max !== undefined && numValue > attribute.validation.max) {
      return { isValid: false, error: `${attribute.label} must be at most ${attribute.validation.max}` };
    }
  }

  if (attribute.type === 'select' && value && !attribute.options?.includes(value)) {
    return { isValid: false, error: `${attribute.label} must be one of the available options` };
  }

  if (attribute.type === 'multiselect' && value && Array.isArray(value)) {
    const invalidOptions = value.filter(v => !attribute.options?.includes(v));
    if (invalidOptions.length > 0) {
      return { isValid: false, error: `${attribute.label} contains invalid options: ${invalidOptions.join(', ')}` };
    }
  }

  return { isValid: true };
}
